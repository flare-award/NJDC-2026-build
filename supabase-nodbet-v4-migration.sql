-- ---------------------------------------------------------
-- 1. Флаг разового пересчёта ставок (пункт 8).
--    Каждый клиент один раз перепроверит свои уже рассчитанные ставки
--    против актуальных результатов каток и исправит ошибки прошлого
--    бага (неверный победитель на промежуточных счётах).
-- ---------------------------------------------------------
alter table if exists nodbet_profiles
  add column if not exists bet_reconcile_v1_done boolean not null default false;

-- Убедимся, что колонка есть и у свежих профилей.
alter table if exists nodbet_profiles
  alter column bet_reconcile_v1_done set default false;

-- ---------------------------------------------------------
-- 2. Статусы ставок: ничего менять не нужно — колонка status
--    имеет тип text. Просто документируем допустимые значения:
--      pending | won | lost | refunded | cancelled
--    (cancelled — новый статус для отменённых ставок на будущие матчи).
--    Если у вас вдруг висит CHECK-ограничение на status — снимем его:
-- ---------------------------------------------------------
do $$
declare
  c record;
begin
  for c in
    select conname
    from pg_constraint
    where conrelid = 'nodbet_bets'::regclass
      and contype = 'c'
      and pg_get_constraintdef(oid) ilike '%status%'
  loop
    execute format('alter table nodbet_bets drop constraint if exists %I', c.conname);
  end loop;
end $$;

-- ---------------------------------------------------------
-- 3. Подстраховка: чтобы «отменённые»/«возвращённые» ставки не
--    засоряли подсчёт total_won — это считается на клиенте по
--    статусу won, так что БД трогать не нужно.
-- ---------------------------------------------------------

-- ---------------------------------------------------------
-- 4. Гарантируем, что колонки рулеткиbonus_id/is_negative
--    присутствуют (для корректного отображения истории спинов
--    новых режимов) — на всякий случай дублируем из fix-migration.
-- ---------------------------------------------------------
alter table if exists nodbet_roulette_spins
  add column if not exists bonus_id text not null default 'normal';
alter table if exists nodbet_roulette_spins
  add column if not exists is_negative boolean not null default false;
alter table if exists nodbet_roulette_spins
  alter column won_coins type bigint;

