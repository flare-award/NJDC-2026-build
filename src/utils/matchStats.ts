// Определение овертайма по счёту раундов (пункт 6).
// В CS2 обычная карта играется до 13 раундов. Если у одной из команд
// раундов больше 13 (например 16:14 или 12:16) — значит был овертайм.
// Здесь score_a / score_b трактуются как раунды на карте.
export function hadOvertime(scoreA: number, scoreB: number): boolean {
  return scoreA > 13 || scoreB > 13;
}
